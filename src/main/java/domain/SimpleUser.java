package domain;

public class SimpleUser {
	String name;
	int age;

	public SimpleUser(String name, int age) {
		this.name = name;
		this.age = age;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	@Override
	public String toString() {
		return "";
	}
}
